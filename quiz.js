let lista = [];
let atual = 0;
let pontos = 0;


function iniciar() {

    if (NIVEL === "facil") {
        lista = perguntas_facil;
    }
    else if (NIVEL === "medio") {
        lista = perguntas_medio;
    }
    else {
        lista = perguntas_dificil;
    }

    lista.sort(() => Math.random() - 0.5);

    mostrar();
}

function mostrar() {

    let dados = lista[atual];

    document.getElementById("texto-pergunta").innerText =
        (atual + 1) + ") " + dados.pergunta;

    let caixa = document.getElementById("caixa-opcoes");

    caixa.innerHTML = "";

    dados.opcoes.forEach((opcao, i) => {

        let div = document.createElement("div");

        div.className = "option";

        div.innerHTML = `
            <div class="option-card">
                <img src="${opcao.imagem}">
            </div>

            <div class="option-label">
                ${opcao.texto}
            </div>
        `;

        div.onclick = function () {

            if (i === dados.certa) {
                pontos++;
            }

            atual++;

            if (atual < lista.length) {

                mostrar();

            } else {

                finalizar();

            }
        };

        caixa.appendChild(div);

    });

}


function finalizar() {
    let porcentagem = (pontos / lista.length) * 100;

    let mensagem = "";
    let icone = "";
    let corTexto = "";

    if (porcentagem >= 50) {
        mensagem = "PARABÉNS! VOCÊ FOI INCRÍVEL!";
        icone = "🏆✨";
        corTexto = "#4CAF50"; // Verde
    } else {

        mensagem = "BOM ESFORÇO! VAMOS TENTAR DE NOVO?";
        icone = "🦦💪"; // Mascote dando força
        corTexto = "#F44336"; // Vermelho
    }

    let overlay = document.createElement("div");
    overlay.className = "popup-overlay";

    let popupBox = document.createElement("div");
    popupBox.className = "popup-box";

    // Preenche o Pop-up com HTML
    popupBox.innerHTML = `
        <div class="popup-icone">${icone}</div>
        <h1 style="color: ${corTexto};" class="popup-titulo">${mensagem}</h1>

        <div class="popup-botoes">
            <button class="btn-popup btn-jogar-novamente" onclick="window.location.reload()">
                🔄 JOGAR DE NOVO
            </button>
            <button class="btn-popup btn-trocar-nivel" onclick="window.location.href='Tela dificuldade.html'">
                🎚️ TROCAR NÍVEL
            </button>
        </div>
    `;

    // Adiciona o Pop-up na tela
    overlay.appendChild(popupBox);
    document.body.appendChild(overlay);
}

window.onload = iniciar;